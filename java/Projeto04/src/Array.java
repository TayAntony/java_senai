public class Array {
    public static void main(String[] args) {

        String[] paises = {"Brasil", "Argentina", "Colômbia", "Venezuela"};
        //array é uma sequencia de valores dentro de uma lista/matriz/vetor, etc
        for(int posicao = 0; posicao < paises.length; posicao++){
            System.out.println(paises[posicao]);
        }
    }
}
